# Arithmetic operations with num1 and num2
num1 = 7.25
num2 = 4.6

# Perform operations and round to 1 decimal place
addition = round(num1 + num2, 1)
subtraction = round(num1 - num2, 1)
multiplication = round(num1 * num2, 1)
division = round(num1 / num2, 1)

# Display results
print(f"num1 = {num1}")
print(f"num2 = {num2}")
print()
print(f"Addition: {num1} + {num2} = {addition}")
print(f"Subtraction: {num1} - {num2} = {subtraction}")
print(f"Multiplication: {num1} * {num2} = {multiplication}")
print(f"Division: {num1} / {num2} = {division}")
